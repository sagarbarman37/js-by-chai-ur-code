let str = "string";
console.log(str);
console.log(typeof str); //string

let num = 7242;
console.log(num);
console.log(typeof num); // number

let bool = true;
console.log(bool);
console.log(typeof bool); // boolean

let box;
console.log(box);
console.log(typeof box); // undefined

let box2 = null;
console.log(box2);
console.log(typeof box2); // object

let big = 5432745384345n;
console.log(big);
console.log(typeof big); // bigint

let symb = Symbol("sagar barman"); // Symbol keyword always start with capital S.
console.log(symb);
console.log(typeof symb); // symbol
