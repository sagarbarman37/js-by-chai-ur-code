const month = 3;

switch (month) {
  case 1:
    console.log("january");
    break;

  case 2:
    console.log("febuary");
    break;

  case 3:
    console.log("march");
    break;

  case 4:
    console.log("april");
    break;

  case 5:
    console.log("may");
    break;

  default:
    console.log("please select month");

    break;
}

